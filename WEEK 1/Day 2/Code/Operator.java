public class Operator {
    public static void main(String[] args){
        int x = 10;
        int y = 3;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);

        int z = 5;
        ++z;
        System.out.println(z);

        --z;
        System.out.println(z);

        // System.out.println(x);
    }
}
